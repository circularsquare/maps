def _expand_bundles(table):
    """gtfs sometimes carries a ・-bundled line as one feature (京王線・高尾線・
    相模原線), sometimes as separate per-component features. Register every
    bundle's components individually too, so the table survives that change."""
    out = dict(table)
    for key, en in table.items():
        op, _, jp = key.partition('||')
        if '・' not in jp:
            continue
        jp_parts = [p if p.endswith('線') else p + '線'
                    for p in jp.split('・') if p]
        en_parts = [e.strip() for e in en.split(' / ')]
        if len(jp_parts) == len(en_parts):
            for jpp, enp in zip(jp_parts, en_parts):
                out.setdefault(op + '||' + jpp, enp)   # explicit entries win
    return out


