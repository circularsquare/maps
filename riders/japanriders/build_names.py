# ──────────────────────────────── helpers ──────────────────────────────────
MACRONS = {'ā': 'a', 'ī': 'i', 'ū': 'u', 'ē': 'e', 'ō': 'o',
           'â': 'a', 'î': 'i', 'û': 'u', 'ê': 'e', 'ô': 'o',
           'Ā': 'A', 'Ī': 'I', 'Ū': 'U', 'Ē': 'E', 'Ō': 'O',
           'Â': 'A', 'Î': 'I', 'Û': 'U', 'Ê': 'E', 'Ô': 'O'}


def clean_en(s):
    """OSM name:en -> plain-ASCII station label."""
    for k, v in MACRONS.items():
        s = s.replace(k, v)
    s = re.sub(r'\s*\(.*?\)\s*', ' ', s)        # drop parenthetical notes
    s = re.sub(r'\s+[Ss]tation$', '', s)        # drop trailing " Station"
    return ' '.join(s.split()).strip()


def norm(s):
    """Normalise a Japanese station name for matching: fullwidth->halfwidth,
    drop bracketed sub-names, and unify the ヶ/ケ and small-tsu variants that
    differ between sources."""
    s = unicodedata.normalize('NFKC', s)
    s = re.sub(r'[(〈《].*?[)〉》]', '', s)        # 諫早（雲仙・島原口） → 諫早
    return (s.replace('ヶ', 'ケ').replace('ヵ', 'カ')
             .replace('ッ', 'ツ').replace('・', '').replace(' ', ''))


def ends(geom):
    t, c = geom['type'], geom['coordinates']
    return (c[0], c[-1]) if t == 'LineString' else (c[0][0], c[-1][-1])


# ──────────────────────────────── build ────────────────────────────────────
def main():
    fc = json.load(open(HERE + '/data/segments.geojson', encoding='utf-8'))
    osm = json.load(open(HERE + '/data/osm_jp_stations.json', encoding='utf-8'))['elements']

    # OSM stations grouped by normalised Japanese name
    by_name = defaultdict(list)
    for e in osm:
        t = e.get('tags', {})
        nm, en = t.get('name'), t.get('name:en')
        if nm and en:
            by_name[norm(nm)].append((e['lat'], e['lon'], clean_en(en)))

    # one robust coordinate per (op, station): median of all observed points
    obs = defaultdict(list)
    for f in fc['features']:
        p = f['properties']
        a, b = ends(f['geometry'])
        for nm, c in ((p['from'], a), (p['to'], b)):
            if nm:
                obs[(p['op'], nm)].append(c)
    # the JR ridership bubbles carry intermediate stations the coarse national
    # segments omit — fold them in so every bubble gets an English name too
    sfc_path = HERE + '/data/stations.geojson'
    bubble_ops, bubble_lines = set(), set()
    if os.path.exists(sfc_path):
        for f in json.load(open(sfc_path, encoding='utf-8'))['features']:
            p = f['properties']
            bubble_ops.add(p['op'])
            bubble_lines.add(p['op'] + '||' + p['line'])
            if p.get('station'):
                obs[(p['op'], p['station'])].append(f['geometry']['coordinates'])
    coord = {k: (statistics.median(x[0] for x in v),
                 statistics.median(x[1] for x in v)) for k, v in obs.items()}

    stations, ambiguous, unmatched = {}, [], []
    for (op, nm), (lon, lat) in coord.items():
        cands = by_name.get(norm(nm))
        if not cands:
            if (op + '||' + nm) in STATIONS_MANUAL:
                stations[op + '||' + nm] = STATIONS_MANUAL[op + '||' + nm]
            else:
                unmatched.append((op, nm))
            continue
        ens = {c[2] for c in cands}
        if len(ens) == 1:
            stations[op + '||' + nm] = cands[0][2]
        else:
            best = min(cands, key=lambda c: math.hypot(c[0] - lat, c[1] - lon))
            stations[op + '||' + nm] = best[2]
            ambiguous.append((op, nm, best[2], sorted(ens)))

    # cover lines / operators / remarks actually present, and warn on any gap
    op_present = sorted({f['properties']['op'] for f in fc['features']} | bubble_ops)
    ln_present = sorted({f['properties']['op'] + '||' + f['properties']['line']
                         for f in fc['features']} | bubble_lines)
    rm_present = sorted({f['properties'].get('remark', '') for f in fc['features']}
                        - {''})
    operators = {o: OPERATORS_EN[o] for o in op_present if o in OPERATORS_EN}
    lines = {k: LINES_EN[k] for k in ln_present if k in LINES_EN}
    remarks = {r: REMARKS_EN[r] for r in rm_present if r in REMARKS_EN}

    out = {'operators': operators, 'lines': lines,
           'stations': stations, 'remarks': remarks}
    json.dump(out, open(HERE + '/data/names_en.json', 'w', encoding='utf-8'),
              ensure_ascii=False, separators=(',', ':'), sort_keys=True)

    print('names_en.json written')
    print(f'  operators : {len(operators)}/{len(op_present)}'
          + ('' if len(operators) == len(op_present) else '  *** MISSING ***'))
    print(f'  lines     : {len(lines)}/{len(ln_present)}'
          + ('' if len(lines) == len(ln_present) else '  *** MISSING ***'))
    print(f'  remarks   : {len(remarks)}/{len(rm_present)}'
          + ('' if len(remarks) == len(rm_present) else '  *** MISSING ***'))
    print(f'  stations  : {len(stations)}/{len(coord)} '
          f'({100 * len(stations) // len(coord)}%)  '
          f'[{len(ambiguous)} disambiguated by coord]')
    for o in op_present:
        if o not in OPERATORS_EN:
            print('  MISSING operator:', o)
    for k in ln_present:
        if k not in LINES_EN:
            print('  MISSING line:', k)
    for r in rm_present:
        if r not in REMARKS_EN:
            print('  MISSING remark:', r)
    if unmatched:
        print(f'  {len(unmatched)} unmatched stations (fall back to Japanese):')
        for op, nm in sorted(unmatched):
            print(f'      {op}||{nm}')


if __name__ == '__main__':
    main()
